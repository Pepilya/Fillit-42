#include "libft.h"

char	*ft_strdup(const char *str)
{
	int	len;
	char*dup;
	int	i;

	len = 0;
	while (str[len])
	{
		len++;
	}
	dup = (char*)malloc(len + 1);
	if (dup == NULL)
		return (NULL);
	i = 0;
	while (str[i])
	{
		dup[i] = str[i];
		i++;
	}
	dup[i] = 0;
	return (dup);
}
